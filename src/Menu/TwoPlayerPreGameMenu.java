    private void btnSizeMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSizeMousePressed
        // TODO add your handling code here:
        if(sizeOfBoard == 8){
            sizeOfBoard = 10;
            btnSize.setText("10");
        }else if(sizeOfBoard == 10){
            sizeOfBoard = 12;
            btnSize.setText("12");
        }else{
            sizeOfBoard = 8;
            btnSize.setText("8");
        }
           
    }//GEN-LAST:event_btnSizeMousePressed

